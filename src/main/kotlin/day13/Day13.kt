fun part1(input: String): Int {
    return input.blocks().mapIndexed { index, value ->
        if (comparePackets(value) < 0) {
            index + 1
        } else {
            0
        }
    }.sum()
}

fun part2(input: String): Int {
    val sortedPackages = input.lines().map { Packet.from(it) }.sortedWith(PacketComparator())
    val smallDividerPosition = findPosition(sortedPackages, Packet.from("[[2]]"))
    val bigDividerPosition = findPosition(sortedPackages, Packet.from("[[6]]")) + 1
    return smallDividerPosition * bigDividerPosition
}

fun findPosition(sortedPackages: List<Packet>, packet: Packet): Int {
    sortedPackages.zipWithNext().forEachIndexed { index, pair ->
        val comp1 = pair.first.compare(packet)
        val comp2 = packet.compare(pair.second)
        if (comp1 <= 0 && comp2 <= 0) {
            return index + 2
        }
    }
    return sortedPackages.size
}

class PacketComparator : Comparator<Packet> {
    override fun compare(o1: Packet, o2: Packet): Int = o1.compare(o2)

}

fun comparePackets(input: String): Int {
    val (line1, line2) = input.lines()
    return Packet.from(line1).compare(Packet.from(line2))
}

sealed interface Packet {
    // - is right, + is wrong, 0 is equal
    fun compare(other: Packet): Int

    data class Atom(val value: Int) : Packet {
        override fun compare(other: Packet): Int = when (other) {
            is Atom -> value - other.value
            is Multiple -> Multiple(listOf(this)).compare(other)
        }
    }

    data class Multiple(val values: List<Packet>) : Packet {
        override fun compare(other: Packet): Int {
            when (other) {
                is Atom -> return compare(Multiple(listOf(other)))
                is Multiple -> {
                    values.zip(other.values).forEach { items ->
                        val compareResult = items.first.compare(items.second)
                        if (compareResult != 0) {
                            return compareResult
                        }
                    }
                    return values.size - other.values.size
                }
            }
        }
    }

    companion object {
        fun from(line: String): Packet = Parser(Lexer(line)).parse()
    }
}

class Parser(private val lexer: Lexer) {
    private var buffer: Token? = null
    fun parse(): Packet = parse(next())

    private fun parse(currentToken: Token): Packet {
        return when (currentToken) {
            is Token.ListStart -> parseList()
            is Token.Number -> Packet.Atom(currentToken.value)
            else -> throw IllegalArgumentException("wrong token '$currentToken' in '${lexer.line}'")
        }
    }

    private fun parseList(): Packet {
        val items = mutableListOf<Packet>()
        if (match(Token.ListEnd)) {
            return Packet.Multiple(items)
        }
        items.add(parse())
        var currentToken = next()
        while (currentToken != Token.ListEnd) {
            check(currentToken == Token.Comma)
            items.add(parse())
            currentToken = lexer.getNext()
        }
        return Packet.Multiple(items)
    }

    private fun match(type: Token): Boolean {
        if (peek() == type) {
            next()
            return true
        }
        return false
    }

    private fun peek(): Token {
        if (buffer == null) {
            buffer = next()
        }
        return buffer!!
    }

    private fun next(): Token {
        val currentBuffer = buffer
        if (currentBuffer != null) {
            buffer = null
            return currentBuffer
        }
        return lexer.getNext()
    }
}

sealed interface Token {
    data class Number(val value: Int) : Token
    object ListStart : Token
    object ListEnd : Token
    object Comma : Token
    object EndOfPacket : Token
}

class Lexer(val line: String) {
    private var index: Int = 0
    private var openCloseParens: Int = 0

    fun getNext(): Token {
        if (isAtEnd()) {
            if (openCloseParens > 0) {
                return closeParen()
            }
            return Token.EndOfPacket
        }
        if (peekChar().isDigit()) {
            return number()
        }
        return when (val c = nextChar()) {
            '[' -> openParen()
            ']' -> closeParen()
            ',' -> Token.Comma
            else -> throw IllegalArgumentException("unknown character '$c'")
        }
    }

    private fun peekChar(): Char = line[index]

    private fun nextChar(): Char = peekChar().also { index += 1 }

    private fun openParen(): Token {
        openCloseParens += 1
        return Token.ListStart
    }

    private fun closeParen(): Token {
        openCloseParens -= 1
        return Token.ListEnd
    }

    private fun number(): Token {
        var lexeme = ""
        while (peekChar().isDigit()) {
            lexeme += nextChar()
        }
        return Token.Number(lexeme.toInt())
    }

    private fun isAtEnd(): Boolean = line.length <= index
}

